package labs;


public class logical {
    public static void main(String[] args){
        boolean a = false;
        boolean b = true;
        System.out.println("a="+a+"  b="+b);
        System.out.println("a|b"+(a|b) );
        System.out.println("a&b"+(a&b));
        System.out.println("a^b"+(a^b));
        System.out.println("!a="+!a);

    }
}
